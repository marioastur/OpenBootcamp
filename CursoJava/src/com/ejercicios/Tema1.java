package com.ejercicios;

public class Tema1 {
    public static void main(String[] args) {

        // Especificamos el tipo de dato, luego operador, asignación y valor
        // 1. numéricos
        // 1.1 enteros
        byte variable1 = 10;
        short variable2 = 15;
        int variable3 = 2343;
        long variable4 = 234223;

        // 1.2 decimales
        float variable5 = 5.5f;
        double variable6 = 10.54d;

        // 2. booleanos
        boolean variable7 = true; // o false

        // 3. texto
        char variable8 = 'A';
        String variable9 = "Esto es una cadena de texto"; // String es una clase. Por eso se escribe con mayúscula

        // Imprimir todas las variables
        System.out.println(variable1);
        System.out.println(variable2);
        System.out.println(variable3);
        System.out.println(variable4);
        System.out.println(variable5);
        System.out.println(variable6);
        System.out.println(variable7);
        System.out.println(variable8);
        System.out.println(variable9);

    }
}
